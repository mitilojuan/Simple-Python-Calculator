x = input('Enter operator: ')
y = float(input('Enter first number: '))
z = float(input('Enter second number: '))

if x == '+':
	print('Result is: ')
	print(y+z)

elif x == '-':
	print('Result is: ')
	print(y-z)

elif x == '*':
	print('Result is: ')
	print(y*z)

elif x == '/':
	print('Result is: ')
	print(y/z)